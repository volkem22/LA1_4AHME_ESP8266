uint8_t cmdExecuteCommand(CliComPort *cliComPort)
{
    const char *cmd = cliGetFirstToken(cliComPort);
    if(cmd == NULL)
        return 0;


    if(strcmp(cmd, "cls") == 0)
        cliClearScreen(cliComPort);
    
        return 0;
}